DROP TABLE IF EXISTS USER;
DROP TABLE IF EXISTS ITEM;
DROP TABLE IF EXISTS BOX;
DROP TABLE IF EXISTS PALLET;
DROP TABLE IF EXISTS WAREHOUSE;
DROP TABLE IF EXISTS DELIVERY_NOTE;
CREATE TABLE USER(id INT PRIMARY KEY AUTO_INCREMENT, name VARCHAR(100), email VARCHAR(100), password VARCHAR(100)); 
CREATE TABLE ITEM(id INT PRIMARY KEY AUTO_INCREMENT, item_no VARCHAR(6), box_id INT, pallet_id INT, pcs_in_box INT, item_weight DOUBLE);
CREATE TABLE BOX(id INT PRIMARY KEY AUTO_INCREMENT, box_type VARCHAR(6), boxes_in_row INT, rows_on_pallet INT, box_weight DOUBLE);
CREATE TABLE PALLET(id INT PRIMARY KEY AUTO_INCREMENT, pallet_type VARCHAR(6), pallet_weight DOUBLE, roof_weight DOUBLE, stackable BOOLEAN);
CREATE TABLE WAREHOUSE(id INT PRIMARY KEY AUTO_INCREMENT, item_no VARCHAR(6), stock INT);
CREATE TABLE DELIVERY_NOTE(id INT PRIMARY KEY AUTO_INCREMENT, item_no VARCHAR(6), stock INT);